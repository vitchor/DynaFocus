//  Pattern for the .h files:
//
//
//  #import ... all NECESSARY imports
//
//  #define ... all necessary defines
//
//  @interface ClassName : NSObject {
//    
//    Variables declaration order:
//    
//        FIRST: all primitive variables (ex: int, long, float, bool ...)
//    
//        SECOND: all iOS default classes (ex: NSClasses, UIView ...)
//                          
//        THIRD: all the other classes we've implemented (ex: FOFTableController, ProfileController ...)
//
//
//       ps: going deeper in the organization, you can also put all the "same class types" together, like:  NSView view1
//  }                                                                                                       NSView view2
//                                                                                                          NSView view3
//
//  Properties declaration order: same as above described
//
//    FIRST: ... ex: @property() int
//                    
//    SECOND: ... ex: @property() UIView
//                                       
//    THIRD: ... ex: @property() FOFTableController
//
//
//  Finally, all the class METHODS go here
//
//  @end
