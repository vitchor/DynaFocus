//
//  Pattern for the .m files:
//
//
//  #import "ClassName.h"  (this is the ONLY import the .m files should have!)
//
//  #defines NO DEFINES (all defines go into the .h file!)
//
//
//  @implementation ClassName
//
//  @synthesize prop1, prop2, prop3, ... (please put the props in the same order as in the .h file  =] )
//
//  Class methods order (you may notice that this is a chronological order ;p ):
//
//      FIRST: all init methods
//
//      SECOND: viewDidLoad
//      THIRD: viewWillAppear
//      FOURTH: viewDidAppear
//
//      FIFTH: viewWillDisappear
//      SIXTH: viewDidDisappear
//      SEVENTH: viewDidUnload
//
//      EIGHTH: other methods
//
//      NINTH: utilities methods ("methods that are often used by everyone", by Victor Gomes de Oliveira)
//
//      TENTH: delloc (please put the releases in the same order as in the .h file  =] ) 
//
//  @end
